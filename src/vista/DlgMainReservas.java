    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int fila = tblReservas.getSelectedRow();

    if (fila == -1) {
        JOptionPane.showMessageDialog(
                this,
                "Seleccione una reserva."
        );
        return;
    }

    int numeroReserva = Integer.parseInt(
            tblReservas.getValueAt(fila, 0).toString()
    );

    int indiceReal = buscarIndicePorNumero(numeroReserva);

    if (indiceReal == -1) {
        JOptionPane.showMessageDialog(
                this,
                "No se encontró la reserva."
        );
        return;
    }

    DlgNewReserva ventana = new DlgNewReserva(
            (java.awt.Frame) getParent(),
            true,
            reservaStore,
            listaClientes,
            listaVehiculos,
            2,
            indiceReal
    );

    ventana.setLocationRelativeTo(this);
    ventana.setVisible(true);

    mostrarTabla(txtBuscar.getText());
    }//GEN-LAST:event_btnEditarActionPerformed

