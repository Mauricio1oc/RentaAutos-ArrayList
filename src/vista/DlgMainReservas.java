    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
     int fila = tblReservas.getSelectedRow();

    if (fila == -1) {
        JOptionPane.showMessageDialog(
                this,
                "Seleccione una reserva."
        );
        return;
    }

    int numeroReserva = Integer.parseInt(
            tblReservas.getValueAt(fila, 0).toString()
    );

    int respuesta = JOptionPane.showConfirmDialog(
            this,
            "¿Desea eliminar la reserva número "
                    + numeroReserva + "?",
            "Confirmar eliminación",
            JOptionPane.YES_NO_OPTION
    );

    if (respuesta != JOptionPane.YES_OPTION) {
        return;
    }

    int indiceReal = buscarIndicePorNumero(numeroReserva);

    if (reservaStore.eliminar(indiceReal)) {
        JOptionPane.showMessageDialog(
                this,
                "Reserva eliminada correctamente."
        );

        mostrarTabla(txtBuscar.getText());
    }  
    }//GEN-LAST:event_btnEliminarActionPerformed

