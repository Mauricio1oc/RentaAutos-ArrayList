    private void mostrarTabla(String textoBuscar) {

    String[] columnas = {
        "N.º Reserva",
        "Fecha",
        "Cédula",
        "Cliente",
        "Placa",
        "Vehículo",
        "Días",
        "Precio por día",
        "Subtotal",
        "Impuesto",
        "Total"
    };

    modelo = new DefaultTableModel(null, columnas) {
        @Override
        public boolean isCellEditable(int row, int column) {
            return false;
        }
    };

    String filtro = textoBuscar.trim().toLowerCase();

    for (Reserva reserva : reservaStore.getListaReservas()) {

        String numero = String.valueOf(reserva.getNumReserva());
        String cedula = String.valueOf(
                reserva.getCliente().getCedula()
        );
        String nombreCliente =
                reserva.getCliente().getNombre();
        String placa =
                reserva.getVehiculo().getPlaca();
        String vehiculo =
                reserva.getVehiculo().getMarca()
                + " "
                + reserva.getVehiculo().getModelo();

        boolean coincide =
                numero.contains(filtro)
                || cedula.toLowerCase().contains(filtro)
                || nombreCliente.toLowerCase().contains(filtro)
                || placa.toLowerCase().contains(filtro)
                || vehiculo.toLowerCase().contains(filtro);

        if (filtro.isEmpty() || coincide) {

            Object[] fila = {
                reserva.getNumReserva(),
                reserva.getFechaReserva(),
                reserva.getCliente().getCedula(),
                reserva.getCliente().getNombre(),
                reserva.getVehiculo().getPlaca(),
                vehiculo,
                reserva.getCantDias(),
                String.format("%.2f", reserva.getPrecioDia()),
                String.format("%.2f", reserva.calcularSubtotal()),
                String.format("%.2f", reserva.getImpuesto()),
                String.format("%.2f", reserva.getTotal())
            };

            modelo.addRow(fila);
        }
    }

    tblReservas.setModel(modelo);
    txtCantidad.setText(
            String.valueOf(reservaStore.cantidadReservas())
    );
}
  private int buscarIndicePorNumero(int numeroReserva) {

    for (int i = 0;
            i < reservaStore.getListaReservas().size();
            i++) {

        if (reservaStore.getListaReservas()
                .get(i)
                .getNumReserva() == numeroReserva) {

            return i;
        }
    }

    return -1;
}  
